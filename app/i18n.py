def tr(key: str, **kwargs) -> str:
    lang_map = _T.get(_LANG, _T["en"])
    base = _T["en"]
    text = lang_map.get(key, base.get(key, key))
    return text.format(**kwargs) if kwargs else text


def lang() -> str:
    return _LANG

